package model;

public class Notas {
	int ra_aluno;
	int cod_disciplina;
	int cod_avaliacao;
	double nota;
	
	public int getRa_aluno() {
		return ra_aluno;
	}
	public void setRa_aluno(int ra_aluno) {
		this.ra_aluno = ra_aluno;
	}
	public int getCod_disciplina() {
		return cod_disciplina;
	}
	public void setCod_disciplina(int cod_disciplina) {
		this.cod_disciplina = cod_disciplina;
	}
	public int getCod_avaliacao() {
		return cod_avaliacao;
	}
	public void setCod_avaliacao(int cod_avaliacao) {
		this.cod_avaliacao = cod_avaliacao;
	}
	public double getNota() {
		return nota;
	}
	public void setNota(double nota) {
		this.nota = nota;
	}
}
